    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (!textNombre.getText().isBlank() && !textEmision.getText().isBlank()) {
            System.out.println("1");
            boolean sePuedeModificar = true;
            if (!ValidarDatos.validarTitulo(textNombre.getText())) {
                sePuedeModificar = false;
            }
            String fechaStr = textEmision.getText();
            int a침o = Integer.parseInt(fechaStr.split("-")[0]);
            int mes = Integer.parseInt(fechaStr.split("-")[1]);
            int dia = Integer.parseInt(fechaStr.split("-")[2]);
            if (ValidarDatos.validarFecha(dia + "-" + mes + "-" + a침o) && sePuedeModificar) {
                System.out.println("2");
                if (ValidarDatos.validarEmision(this.temporada.getFechaEstreno(), this.capitulo.getFechaEmision())) {
                    System.out.println("3");
                    this.capitulo.setFechaEmision(LocalDate.of(a침o, mes, dia));
                    this.capitulo.setTitulo(textNombre.getText());
                    this.haModificado = true;
                    setVisible(false);
                }
            } else {
                setVisible(false);
            }
        }
        setVisible(false);
    }//GEN-LAST:event_btnModificarActionPerformed

